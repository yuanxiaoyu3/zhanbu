function findHexagramByLines(lines) {
    // 根据爻线组合返回对应的卦象
    // lines 是一个包含6个元素的数组，每个元素是 'yin' 或 'yang'
    const hexagramMap = {
        'yang,yang,yang,yang,yang,yang': 'qian',
        'yin,yin,yin,yin,yin,yin': 'kun',
        // ... 添加其他组合
    };
    
    const key = lines.join(',');
    return hexagramData[hexagramMap[key]] || hexagramData.qian; // 默认返回乾卦
}

